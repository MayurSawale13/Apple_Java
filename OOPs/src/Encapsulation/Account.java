package Encapsulation;

public class Account {
private int id,accno,bal;

public int getAccno() {
	return accno;
}

public void setAccno(int accno) {
	this.accno = accno;
}

public int getId() {
	return id;
}

public void setId(int id) {
	this.id = id;
}

public int getBal() {
	return bal;
}

public void setBal(int bal) {
	this.bal = bal;
}

}
