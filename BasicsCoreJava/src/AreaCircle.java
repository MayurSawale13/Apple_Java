
public class AreaCircle {

	public static void main(String[] args) {
		
		float r=5.5f; 
		//decimal value default is double use f postfix to define decimal value 
		
		float pi=3.1415f;
		
		float area =pi*r*r;
		//print area on console
		System.out.println("Area of circle is: "+area);

	}

}
